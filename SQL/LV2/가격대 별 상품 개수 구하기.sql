SELECT 
    CASE WHEN P.PRICE < 10000 THEN '0'
         WHEN P.PRICE < 20000 THEN '10000'
         WHEN P.PRICE < 30000 THEN '20000'
         WHEN P.PRICE < 40000 THEN '30000'
         WHEN P.PRICE < 50000 THEN '40000'
         WHEN P.PRICE < 60000 THEN '50000'
         WHEN P.PRICE < 70000 THEN '60000'
         WHEN P.PRICE < 80000 THEN '70000'
         WHEN P.PRICE < 90000 THEN '80000'
         WHEN P.PRICE < 100000 THEN '90000'
         END AS PRICE_GROUP, COUNT(*) AS PRODUCTS
FROM PRODUCT P
GROUP BY PRICE_GROUP
ORDER BY PRICE_GROUP ASC
